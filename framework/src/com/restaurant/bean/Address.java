package com.restaurant.bean;

public class Address {
	private int id;
	private int m_id;
	private String address;
	private String concacts;
	private String phone;
	private int default_flag;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getM_id() {
		return m_id;
	}
	public void setM_id(int m_id) {
		this.m_id = m_id;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getConcacts() {
		return concacts;
	}
	public void setConcacts(String concacts) {
		this.concacts = concacts;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public int getDefault_flag() {
		return default_flag;
	}
	public void setDefault_flag(int default_flag) {
		this.default_flag = default_flag;
	}
	
	
}
